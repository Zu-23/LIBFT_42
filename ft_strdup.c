#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		size;
	char	*str;
	int		i;

	i = 0;
	size = 0;
	while (s1[size])
		size++;
	str = (char *)malloc(sizeof(char) * (size + 1));
	if (!str)
		return (NULL);
	while (i < size)
	{
		str[i] = s1[i];
		i++;
	}
	str[size] = '\0';
	return (str);
}
